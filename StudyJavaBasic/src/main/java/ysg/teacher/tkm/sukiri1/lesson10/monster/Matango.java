package ysg.teacher.tkm.sukiri1.lesson10.monster;

public class Matango {
	/** 名前 */
	private String name;
	/** HP */
	private int hp;
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name セットする name
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return hp
	 */
	public int getHp() {
		return hp;
	}
	/**
	 * @param hp セットする hp
	 */
	public void setHp(int hp) {
		this.hp = hp;
	}



}
