package ysg.teacher.tkm.sukiri1.lesson9;

/**
 * 剣クラス。
 * @author 作成者の名前
 */
public class Sword {
	public String name;
	public int damage;
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name セットする name
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return damage
	 */
	public int getDamage() {
		return damage;
	}
	/**
	 * @param damage セットする damage
	 */
	public void setDamage(int damage) {
		this.damage = damage;
	}
}
