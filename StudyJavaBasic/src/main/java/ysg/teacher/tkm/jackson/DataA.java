package ysg.teacher.tkm.jackson;

public class DataA {

	private int id;
	private String name;
	private String[] sikaku;
	/**
	 * @return id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id セットする id
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name セットする name
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return sikaku
	 */
	public String[] getSikaku() {
		return sikaku;
	}
	/**
	 * @param sikaku セットする sikaku
	 */
	public void setSikaku(String[] sikaku) {
		this.sikaku = sikaku;
	}


}
